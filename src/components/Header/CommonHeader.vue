
<template>
  <div>
    <header class="header">
      <!-- 头部的第一行 -->
      <div class="top">
        <div class="container">
          <div class="loginList">
            <p>尚品汇欢迎您！</p>
            <p v-if="!getUserName">
              <span>请</span>
              <router-link to="/login">登录</router-link>
              <router-link to="/register" class="register"
                >免费注册</router-link
              >
            </p>
            <p v-else>
              <a>{{ getUserName }}</a>
              <a class="register" @click="loginout">退出登录</a>
            </p>
          </div>
          <div class="typeList">
            <a href="###">我的订单</a>
            <a href="###">我的购物车</a>
            <a href="###">我的尚品汇</a>
            <a href="###">尚品汇会员</a>
            <a href="###">企业采购</a>
            <a href="###">关注尚品汇</a>
            <a href="###">合作招商</a>
            <a href="###">商家后台</a>
          </div>
        </div>
      </div>
      <!--头部第二行 搜索区域-->
      <div class="bottom">
        <h1 class="logoArea">
          <router-link class="logo" title="尚品汇" to="/home">
            <img src="./images/logo.png" alt="" />
          </router-link>
        </h1>
        <div class="searchArea">
          <form action="###" class="searchForm">
            <input
              v-model="keyword"
              type="text"
              id="autocomplete"
              class="input-error input-xxlarge"
            />
            <!-- 这里需要跳转到搜索页面 -->
            <button
              class="sui-btn btn-xlarge btn-danger"
              type="button"
              @click="goSearch"
            >
              搜索
            </button>
          </form>
        </div>
      </div>
    </header>
  </div>
</template>

<script>
export default {
  name: "common-header",
  mounted() {
    // 全局事件总线监听是否删除input里面的值
    this.$root.$on("clearInput", () => {
      this.keyword = "";
    });
  },
  destroyed() {
    // 关闭全局事件总线的的监听
    this.$root.$off("clearInput");
  },
  data() {
    return {
      // input 双向绑定的数据
      keyword: "",
    };
  },
  methods: {
    /**
     * @description: 跳转到搜索页面
     * @return {*}
     */
    goSearch() {
      if (this.keyword != "") {
        // 路由传参
        // 1. 字符串的写法
        // params传参的时候,记得路由要去占位
        // this.$router.push(`/search/${this.keyword}`);
        // 如果是query传参
        // this.$router.push(`/search?kw=${this.keyword}`);
        // 2. 对象的写法(常用)
        let location = {
          // TIPS 这里需要注意,路由里面也需要设置name属性
          // 不能用path的形式
          name: "search",
          params: {
            keyword: this.keyword,
          },
        };
        // 判断有没有query参数需要带
        if (this.$route.query) {
          location.query = this.$route.query;
        }
        this.$router.push(location);
      } else {
        return;
      }
    },
    // 退出登录
    async loginout() {
      try {
        await this.$store.dispatch("userLoginout");
        alert("退出成功");
        this.$router.push("/home");
      } catch (error) {
        alert(error);
      }
    },
  },
  computed: {
    getUserName() {
      return this.$store.state.user.userInfo.name;
    },
  },
};
</script>

